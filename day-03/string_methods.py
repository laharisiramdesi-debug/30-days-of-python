s=" cybersecurity is easy "
s1="security"
print(s.upper())#converts every character into uppercase
print(s.lower())#converts every character into lowercase
print(s.title())#make the first letter of every word into uppercase
print(s1.capitalize())#make the first word of a string into uppercase
print(s.strip())#it removes the spaces from the starting and end of a string
print(s1.replace("e","i"))#it replaces the character by the given character
print(s.find("b"))#it finds the index of first occurence of a word
print(s.count("y"))#it counts how many times the letter occurs
print(s.startswith("cyber"))#it checks whether the word starts with the given word
print(s.endswith("easy"))#it checks whether the word ends with the given word
print(len(s))#it gives the length of string